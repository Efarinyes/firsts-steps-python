# Operacions bàsiques ( suma, resta, multiplicació i divisió ) a partir de dos números facilitats

primer = int(input('Introdueix el primer número: '))
segon = int(input('Introdueix el segon número: '))

print(f'La suma de { primer } + { segon } és: { primer + segon}')
print(f'La resta de { primer } - { segon } és: { primer -segon}')
print(f'El producte de { primer } X { segon } és: { primer * segon }')
print(f'La divisió de { primer } / { segon } dona: { primer / segon }')